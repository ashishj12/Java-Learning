package Operators;

public class Logical {
    public static void main(String[] args) {
        boolean A = true, B = false;

        System.out.println("A && B: " + (A && B));
        System.out.println("A || B: " + (A || B));
        System.out.println("!A: " + (!A));
        System.out.println("!B: " + (!B));
    }
}
