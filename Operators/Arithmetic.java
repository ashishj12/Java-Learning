package Operators;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 5, b = 5;

        // Addition
        System.out.println("a + b = " + (a + b));

        // Subtraction
        System.out.println("a - b = " + (a - b));

        // Multiplication
        System.out.println("a * b = " + (a * b));

        // Division
        System.out.println("a / b = " + (a / b));

        // Modulus
        System.out.println("a % b = " + (a % b));
    }
}
