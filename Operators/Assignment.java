package Operators;

public class Assignment {
    public static void main(String[] args) {
        int a = 1;

        // Assign and add
        a += 2;
        System.out.println("a += 2: " + a);

        // Assign and subtract
        a -= 3;
        System.out.println("a -= 3: " + a);

        // Assign and multiply
        a *= 4;
        System.out.println("a *= 4: " + a);

        // Assign and divide
        a /= 5;
        System.out.println("a /= 5: " + a);

        // Assign and modulus
        a %= 6;
        System.out.println("a %= 6: " + a);
    }
}
