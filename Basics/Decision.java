public class Decision {
    public static void main(String[] args) {
        // only if condition
        int a = 10;
        if (a < 15) {
            System.out.println("Condition is true");
        }

        // if-else condition
        int b = 15;
        if (b > 10) {
            System.out.println("value is greater than given condition");
        } else {
            System.out.println("value is lesser then given condition");
        }
    }
}
